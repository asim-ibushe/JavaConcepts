package com.aib.superthis;

public class SuperThisTheory {

	// 'this' keyword is always used to refer to the current class object
	// 'super' keyword refers to the parent class object

	// 'this' keyword is always used to refer to the current class constructor
	// 'super' keyword refers to the parent class constructor

	// 'this' keyword is always used to refer to the current class instance variable
	// 'super' keyword refers to the parent class variables

	// 'this' keyword is always used to refer to the current class methods
	// 'super' keyword refers to the parent class methods

}
