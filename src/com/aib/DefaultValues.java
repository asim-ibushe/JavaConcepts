package com.aib;

public class DefaultValues {

	boolean booleanDefault;
	byte byteDefault;
	char charDefault;
	short shortDefault;
	int intDefault;
	float floatDefault;
	long longDefault;
	double doubleDefault;

	public static void main(String[] args) {

		DefaultValues t = new DefaultValues();

		System.out.println(t.booleanDefault);
		System.out.println(t.byteDefault);
		System.out.println(t.charDefault);
		System.out.println(t.shortDefault);
		System.out.println(t.intDefault);
		System.out.println(t.floatDefault);
		System.out.println(t.longDefault);
		System.out.println(t.doubleDefault);

	}
}
